export const providerDirectoryData: DirectoryProvider[] = rawProviderDirectoryData.map((p) => {
  if (activeRegionKey !== "kent") return p;

  const transform = (str: string) => {
    if (!str) return str;
    return str
      .replace(/Wiltshire/g, "Kent")
      .replace(/wiltshire/g, "kent");
  };

  const copy = { ...p };
  const textFields: (keyof DirectoryProvider)[] = ["coverageNotes", "editorNotes", "seoTitle", "metaDescription", "displayName"];
  textFields.forEach((field) => {
    const val = copy[field];
    if (val && typeof val === "string") {
      (copy as any)[field] = transform(val);
    }
  });

  if (copy.knownRegions) {
    copy.knownRegions = copy.knownRegions.map(r => r === "Wiltshire" ? "Kent" : r);
  }

  if (copy.providerId === "wessex" || copy.providerId === "toob") {
    copy.wiltshireRelevance = false;
  }

  return copy;
});

// Handles deduplication of raw manual or external provider feeds
export function dedupeProviders(providers: DirectoryProvider[]): DirectoryProvider[] {
  const seenIds = new Set<string>();
  const deduped: DirectoryProvider[] = [];

  for (const p of providers) {
    const canonicalId = p.providerId.toLowerCase().trim();
    if (!seenIds.has(canonicalId)) {
      seenIds.add(canonicalId);
      deduped.push(p);
    } else {
      const existing = deduped.find(ex => ex.providerId === p.providerId);
      if (existing) {
        const mergedNames = Array.from(new Set([...existing.alternativeNames, ...p.alternativeNames, p.providerName, p.displayName]));
        existing.alternativeNames = mergedNames.filter(name => name !== existing.providerName);
      }
    }
  }

  return deduped;
}
