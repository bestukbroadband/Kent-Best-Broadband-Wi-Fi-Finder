/**
 * Returns availability details for a given offer and postcode prefix.
 * Defaults to "Address check required" if not specifically mapped,
 * or "Likely available" for mainstream providers (BT/EE 5G/Virgin) in urban postcodes.
 */
export function getAvailabilityForOffer(offerId: string, postcodePrefix: string): OfferAvailability {
  const normalizedPrefix = postcodePrefix.toUpperCase().trim();
  
  const match = offerAvailabilityData.find(
    (a) => a.offerId === offerId && a.postcodePrefix.toUpperCase() === normalizedPrefix
  );

  if (match) {
    return match;
  }

  // Fallback heuristics for mainstream national providers which are widely available
  if (offerId === "offer-bt-150" || offerId === "offer-ee-5g") {
    // BT Openreach and cellular coverage are near-universal across Wiltshire town boundaries
    const urbanPrefixes = ["SN1", "SN2", "SN3", "SN5", "SP1", "SP2", "BA14", "BA15", "SN10", "SN11", "SN14", "SN15"];
    if (urbanPrefixes.includes(normalizedPrefix)) {
      return {
        offerId,
        postcodePrefix,
        availabilityStatus: "Likely available",
        availabilityConfidence: "High",
        addressLevelCheckRequired: true,
        notes: "Broad national availability. Individual address line check at checkout is recommended.",
        lastCheckedDate: "2026-06-08"
      };
    }
    return {
      offerId,
      postcodePrefix,
      availabilityStatus: "Address check required",
      availabilityConfidence: "Medium",
      addressLevelCheckRequired: true,
      notes: "Rural network node ranges fluctuate. Detailed address lookup is necessary to verify physical line routing.",
      lastCheckedDate: "2026-06-08"
    };
  }

  if (offerId === "offer-virgin-350") {
    const virginPrefixes = ["SN1", "SN2", "SN3", "SN5", "SP1"];
    if (virginPrefixes.includes(normalizedPrefix)) {
      return {
        offerId,
        postcodePrefix,
        availabilityStatus: "Likely available",
        availabilityConfidence: "High",
        addressLevelCheckRequired: true,
        notes: "Excellent urban cable footprints. Final check recommended before connection.",
        lastCheckedDate: "2026-06-08"
      };
    }
    return {
      offerId,
      postcodePrefix,
      availabilityStatus: "Not currently listed",
      availabilityConfidence: "High",
      addressLevelCheckRequired: false,
      notes: "Virgin Media high-speed street cabling is not currently constructed in this rural region.",
      lastCheckedDate: "2026-06-08"
    };
  }

  // Default case for unmapped altnet and rural combinations
  return {
    offerId,
    postcodePrefix,
    availabilityStatus: "Address check required",
    availabilityConfidence: "Low",
    addressLevelCheckRequired: true,
    notes: "Regional coverage bounds for this provider shift dynamically. Final physical lines must be verified.",
    lastCheckedDate: "2026-06-08"
  };
}
