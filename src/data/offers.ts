export const offersData: Offer[] = rawOffersData.map((offer) => {
  if (activeRegionKey !== "kent") return offer;

  const transform = (str: string) => {
    if (!str) return str;
    return str
      .replace(/Wiltshire/g, "Kent")
      .replace(/wiltshire/g, "kent");
  };

  const copy = { ...offer };
  const textFields: (keyof Offer)[] = ["headline", "shortDescription", "editorVerdict", "editorNotes", "packageName", "bestFor"];
  textFields.forEach((field) => {
    const val = copy[field];
    if (val && typeof val === "string") {
      (copy as any)[field] = transform(val);
    }
  });

  if (copy.thingsToCheck) {
    copy.thingsToCheck = copy.thingsToCheck.map(transform);
  }

  return copy;
}).filter(o => !(activeRegionKey === "kent" && o.providerId === "truespeed"));
